string_name = "Tufail baba"
puts string_name
#String operations
puts string_name.length #=> length of string
puts string_name.include? "baba" #=> check if substring is present
puts string_name[0] #=> first character
puts string_name[0, 5] #=> substring from index 0 to 4
puts string_name.capitalize #=> capitalize first letter
puts string_name.reverse #=> reverse the string
puts string_name.downcase #=> convert to lowercase
puts string_name.upcase #=> convert to uppercase